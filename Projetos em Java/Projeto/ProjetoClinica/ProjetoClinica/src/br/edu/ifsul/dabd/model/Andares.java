package br.edu.ifsul.dabd.model;
public class Andares {
   private int codigo;
   private String numero;

    public Andares() {
    }

    public int getCodigo() {
        return codigo;
    }

    public void setCodigo(int codigo) {
        this.codigo = codigo;
    }

    public String getNumero() {
        return numero;
    }

    public void setNumero(String numero) {
        this.numero = numero;
    }
   
   
}
